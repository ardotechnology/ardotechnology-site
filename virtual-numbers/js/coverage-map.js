document.addEventListener('DOMContentLoaded', () => {
    // 1. Initialize map (disabled interactions)
    const map = L.map('coverage-map', {
        scrollWheelZoom: false,
        dragging: false,
        tap: false,
        touchZoom: false
    }); // We will set view later based on data bounds

    L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        attribution: '&copy; OpenStreetMap contributors'
    }).addTo(map);

    // 2. Enable interactions on click
    map.on('click', () => {
        map.scrollWheelZoom.enable();
        map.dragging.enable();
        if (map.tap) map.tap.enable();
        map.touchZoom.enable();
    });

    // 3. Define custom icon (extracted from KMZ content)
    // ADJUST ICON SIZE HERE (pixels)
    const ICON_SIZE = 20;

    const customIcon = L.icon({
        iconUrl: 'images/icon-1.png',
        iconSize: [ICON_SIZE, ICON_SIZE],
        iconAnchor: [ICON_SIZE / 2, ICON_SIZE], // Center horizontal, bottom vertical
        popupAnchor: [0, -ICON_SIZE] // Popup opens above the icon
    });

    // 4. Plot markers from embedded data
    const markers = [];

    coverageData.forEach(item => {
        const marker = L.marker([item.lat, item.lng], { icon: customIcon }).addTo(map);

        // Remove CDATA tags if present, although browser HTML parser handles them mostly, it's cleaner to check.
        // The extraction script gets existing text which might be clean.
        let desc = item.description || '';

        const popupContent = `
            <div class="popup-content">
                <h3>${item.name}</h3>
                <div>${desc}</div>
            </div>
        `;

        marker.bindPopup(popupContent);
        markers.push(marker);
    });

    // 5. Fit bounds
    if (markers.length > 0) {
        const group = new L.featureGroup(markers);
        map.fitBounds(group.getBounds());
    } else {
        map.setView([23.6345, -102.5528], 5);
    }
});
